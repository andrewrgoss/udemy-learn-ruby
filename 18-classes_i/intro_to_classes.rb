#!/usr/bin/env ruby

#
# INTRO TO CLASSES:
#  - Classes are templates for creating objects
# Why do we need classes?
#  - Ruby's built objects like Arrays or Hashes cannot represent everything
#  - What if we need to represent Users or Cars or Invoices?
#  - The goal of object-oriented design is to use code to model real-life
#   things as classes.
#  - An object is a custom container for data. It is made from a class.
#  - Objects group functionalities into a single unit.
#
#  OBJECT ATTRIBUTES AND METHODS:
#  - Instance variables (attributes) are variables that belong to the object.
#  - Attributes provide information about the object's current state.
#  - Instance methods interact with the object. They also belong to the object.
#  - Encapsulation is a OOP concept that restricts direct access to an object.
#
#  CLASSES AND OBJECTS:
#  - A class is a blueprint, pattern, template or outline for an object.
#  - An object is an instance of the class. It is created from the class.
#  - Instantiation is the process of creating an object from a class.
#  - A class is called an abstract type because it hides the complexity of
#   interacting with the data, much like a method abstracts the complexity
#   of a process.
